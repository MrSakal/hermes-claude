def provider_plugin_dir(hermes_home_override: str | Path | None = None) -> Path:
    home = Path(hermes_home_override) if hermes_home_override else hermes_home()
    return home / "plugins" / "model-providers" / PROVIDER_NAME


def general_plugin_dir(hermes_home_override: str | Path | None = None) -> Path:
    home = Path(hermes_home_override) if hermes_home_override else hermes_home()
    return home / "plugins" / PROVIDER_NAME


def _auto_enable_general_plugin(*, which=None, run=None) -> bool:
    which = which or shutil.which
    run = run or subprocess.run
    executable = which("hermes")
    if not executable:
        return False
    try:
        # The executable is resolved locally and all arguments are fixed.
        result = run(  # nosec B603
            [
                executable,
                "plugins",
                "enable",
                PROVIDER_NAME,
                "--no-allow-tool-override",
            ],
            capture_output=True,
            text=True,
            timeout=30,
        )
    except (OSError, subprocess.SubprocessError):
        return False
    return getattr(result, "returncode", 1) == 0


def _write_owned_dir(destination: Path, init_text: str, manifest_text: str) -> None:
    destination.mkdir(parents=True, exist_ok=False)
    if os.name != "nt":
        os.chmod(destination, 0o700)
    for name, content in (("__init__.py", init_text), ("plugin.yaml", manifest_text)):
        path = destination / name
        path.write_text(content, encoding="utf-8")
        if os.name != "nt":
            os.chmod(path, 0o600)


def _owned(destination: Path, init_text: str, manifest_text: str) -> bool:
    if destination.is_symlink() or not destination.is_dir():
        return False
    try:
        return (
            {path.name for path in destination.iterdir()}
            <= {"__init__.py", "plugin.yaml", "__pycache__"}
            and (destination / "__init__.py").read_text(encoding="utf-8") == init_text
            and (destination / "plugin.yaml").read_text(encoding="utf-8")
            == manifest_text
        )
    except (FileNotFoundError, OSError, UnicodeError):
        return False


def _legacy_owned(destination: Path) -> bool:
    if destination.is_symlink() or not destination.is_dir():
        return False
    try:
        if not {path.name for path in destination.iterdir()} <= {
            "__init__.py",
            "plugin.yaml",
            "__pycache__",
        }:
            return False
        digests = tuple(
            hashlib.sha256((destination / name).read_bytes()).hexdigest()
            for name in ("__init__.py", "plugin.yaml")
        )
        return digests in _LEGACY_OWNED_DIGESTS
    except OSError:
        return False


def install(hermes_home_override: str | Path | None = None) -> dict:
    targets = (
        (
            provider_plugin_dir(hermes_home_override),
            _PROVIDER_INIT_PY,
            _PROVIDER_PLUGIN_YAML,
        ),
        (
            general_plugin_dir(hermes_home_override),
            _GENERAL_INIT_PY,
            _GENERAL_PLUGIN_YAML,
        ),
    )

    # Complete the ownership preflight before changing either discovery surface.
    for destination, init_text, manifest_text in targets:
        if destination.exists() or destination.is_symlink():
            if not (
                _owned(destination, init_text, manifest_text)
                or _legacy_owned(destination)
            ):
                raise RuntimeError(
                    f"Refusing to overwrite unowned plugin directory: {destination}"
                )

    prepared: list[tuple[Path, Path, Path | None]] = []
    installed: list[tuple[Path, Path | None]] = []
    try:
        for destination, init_text, manifest_text in targets:
            destination.parent.mkdir(parents=True, exist_ok=True)
            backup = (
                destination.with_name(f".{destination.name}.previous")
                if destination.exists()
                else None
            )
            if backup and (backup.exists() or backup.is_symlink()):
                raise RuntimeError(f"Refusing existing installation backup: {backup}")
            temp = Path(
                tempfile.mkdtemp(prefix=f".{PROVIDER_NAME}-", dir=destination.parent)
            )
            prepared.append((destination, temp, backup))
            temp.rmdir()
            _write_owned_dir(temp, init_text, manifest_text)

        for destination, temp, backup in prepared:
            if backup:
                os.replace(destination, backup)
            try:
                os.replace(temp, destination)
            except Exception:
                if backup:
                    os.replace(backup, destination)
                raise
            installed.append((destination, backup))
    except Exception:
        for destination, backup in reversed(installed):
            if destination.exists():
                shutil.rmtree(destination)
            if backup and backup.exists():
                os.replace(backup, destination)
        raise
    finally:
        for _, temp, _ in prepared:
            if temp.exists():
                shutil.rmtree(temp)

    for _, backup in installed:
        if backup and backup.exists():
            shutil.rmtree(backup)

    enabled = hermes_home_override is None and _auto_enable_general_plugin()
    result = {
        "status": "installed",
        "provider_path": str(targets[0][0]),
        "general_path": str(targets[1][0]),
        "general_plugin_enabled": bool(enabled),
    }
    if not enabled and hermes_home_override is None:
        result["next_step"] = (
            f"hermes plugins enable {PROVIDER_NAME} --no-allow-tool-override"
        )
    return result


def uninstall(hermes_home_override: str | Path | None = None) -> dict:
    targets = (
        (
            provider_plugin_dir(hermes_home_override),
            _PROVIDER_INIT_PY,
            _PROVIDER_PLUGIN_YAML,
        ),
        (
            general_plugin_dir(hermes_home_override),
            _GENERAL_INIT_PY,
            _GENERAL_PLUGIN_YAML,
        ),
    )
    removed: list[str] = []
    missing: list[str] = []
    refused: list[str] = []
    for destination, init_text, manifest_text in targets:
        if not destination.exists() and not destination.is_symlink():
            missing.append(str(destination))
        elif not _owned(destination, init_text, manifest_text):
            refused.append(str(destination))
        else:
            shutil.rmtree(destination)
            removed.append(str(destination))
    status = "refused" if refused else ("removed" if removed else "not-installed")
    return {
        "status": status,
        "removed": removed,
        "missing": missing,
        "refused": refused,
    }
